    private void TableMainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMainMouseClicked
        currentItem = Optional.of(clientService.getCurrentItem(TableMain));
        loadCurrentItemForm();


    }//GEN-LAST:event_TableMainMouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        currentItem = Optional.empty();

        Client client = new Client();
        client.setName(TxtNombre.getText());
        client.setLastname(TxtApellido.getText());
        client.setAddress(TxtDir.getText());
        clientService.save(client);
        reload();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        currentItem.ifPresent(client -> {
            client.setName(TxtNombre.getText());
            client.setLastname(TxtApellido.getText());
            client.setAddress(TxtDir.getText());
            clientService.update(client);
            currentItem = Optional.empty();
        });
        reload();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (currentItem.isPresent()) {
            clientService.delete(currentItem.get().getId());
            currentItem = Optional.empty();
            reload();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void reload() {
        loadCurrentItemForm();
        clientService.loadDataModel((DefaultTableModel) TableMain.getModel());
    }

    private void loadCurrentItemForm() {
        if (currentItem.isPresent()) {
            TxtNombre.setText(currentItem.get().getName());
            TxtApellido.setText(currentItem.get().getLastname());
            TxtDir.setText(currentItem.get().getAddress());
        } else {
            TxtNombre.setText("");
            TxtApellido.setText("");
            TxtDir.setText("");
        }
    }

