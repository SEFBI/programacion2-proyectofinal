    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        currentItem.ifPresent(item -> {
            try {
                item.setRentalDate(formatter.parse(TxtFRenta.getText()));
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Formato incorrecto de fecha de renta");
                return;
                //throw new RuntimeException(e);
            }
            try {
                item.setReturnDate(formatter.parse(TxtFDevolucion.getText()));
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(null, "Formato incorrecto de fecha de Devolucion");
                return;
            }
            item.setBook((Book) cbLibro.getSelectedItem());
            item.setClient((Client) cbCliente.getSelectedItem());
            item.setStatus(RentStatus.valueOf(cbEstado.getSelectedItem().toString()));
            rentService.update(item);
            currentItem = Optional.empty();
        });
        reload();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (currentItem.isPresent()) {
            rentService.delete(currentItem.get().getId());
            currentItem = Optional.empty();
            reload();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {

            currentItem = Optional.empty();

            if (TxtFRenta.getText().isEmpty() || TxtFDevolucion.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingresa las fechas de renta y devolución.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (cbLibro.getSelectedItem() == null || cbCliente.getSelectedItem() == null || cbEstado.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Por favor selecciona el libro, cliente y estado.", "Selección incompleta", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Rent rent = new Rent();
            rent.setRentalDate(formatter.parse(TxtFRenta.getText()));
            rent.setReturnDate(formatter.parse(TxtFDevolucion.getText()));
            rent.setBook((Book) cbLibro.getSelectedItem());
            rent.setClient((Client) cbCliente.getSelectedItem());
            rent.setStatus((RentStatus) cbEstado.getSelectedItem());

            rentService.save(rent);

            reload();

            JOptionPane.showMessageDialog(this, "Renta registrada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Usa el formato correcto (ej. dd/MM/yyyy).", "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar la renta: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

