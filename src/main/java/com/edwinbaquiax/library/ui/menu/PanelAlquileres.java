    private void reload() {
        loadCurrentItemForm();
        rentService.loadDataModelAlquilados((DefaultTableModel) TableMain.getModel());
        this.rentService.loadClients(cbCliente);
        this.rentService.loadBooks(cbLibro);
        this.rentService.LoadStatus(cbEstado);
    }

    private void loadCurrentItemForm() {
        if (currentItem.isPresent()) {
            Rent rent = currentItem.get();

            TxtFRenta.setText(rent.getRentalDate() != null ? formatter.format(rent.getRentalDate()) : "");
            TxtFDevolucion.setText(rent.getReturnDate() != null ? formatter.format(rent.getReturnDate()) : "");

            if (rent.getClient() != null) {
                cbCliente.setSelectedItem(rent.getClient());

            } else {
                cbCliente.setSelectedIndex(-1);
            }

            if (rent.getStatus() != null) {
                cbEstado.setSelectedItem(rent.getStatus());

            } else {
                cbEstado.setSelectedIndex(-1);
            }

            if (rent.getBook() != null) {
                cbLibro.setSelectedItem(rent.getBook());

            } else {
                cbLibro.setSelectedIndex(-1);
            }

            SwingUtilities.invokeLater(() -> {
                cbCliente.revalidate();
                cbCliente.repaint();
                cbEstado.revalidate();
                cbEstado.repaint();
                cbLibro.revalidate();
                cbLibro.repaint();
            });

        } else {

            TxtFRenta.setText("");
            TxtFDevolucion.setText("");
            cbCliente.setSelectedIndex(-1);
            cbEstado.setSelectedIndex(-1);
            cbLibro.setSelectedIndex(-1);
        }
    }

