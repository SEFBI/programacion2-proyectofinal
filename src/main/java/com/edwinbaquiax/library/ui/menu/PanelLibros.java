    private void reload() {
        loadCurrentItemForm();
        bookService.loadDataModel((DefaultTableModel) TableMain.getModel());
        this.bookService.LoadCategories(cbCategory);
        this.bookService.LoadAuthors(cbAuthor);
        
    }

    private void loadCurrentItemForm() {
        if (currentItem.isPresent()) {
            Book book = currentItem.get();

            TxtTitle.setText(book.getTitle()!= null ? book.getTitle() : "");
            TxtCode.setText(book.getCode()!= null ?book.getCode() : "");

            if (book.getCategory()!= null) {
                cbCategory.setSelectedItem(book.getCategory());

            } else {
                cbCategory.setSelectedIndex(-1);
            }

         

            if (book.getAuthor()!= null) {
                cbAuthor.setSelectedItem(book.getAuthor());

            } else {
                cbAuthor.setSelectedIndex(-1);
            }

            SwingUtilities.invokeLater(() -> {
                cbCategory.revalidate();
                cbCategory.repaint();
       
                cbAuthor.revalidate();
                cbAuthor.repaint();
            });

        } else {

            TxtTitle.setText("");
            TxtCode.setText("");
            cbCategory.setSelectedIndex(-1);
            
            cbAuthor.setSelectedIndex(-1);
        }
    }

