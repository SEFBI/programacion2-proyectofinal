    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        currentItem.ifPresent(item -> {
            
            item.setTitle(TxtTitle.getText());
            item.setCode(TxtCode.getText());
            item.setAuthor((Author) cbAuthor.getSelectedItem());
            item.setCategory((Category) cbCategory.getSelectedItem());
            
            bookService.update(item);
            currentItem = Optional.empty();
        });
        reload();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (currentItem.isPresent()) {
            bookService.delete(currentItem.get().getId());
            currentItem = Optional.empty();
            reload();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {

            currentItem = Optional.empty();

            if (TxtTitle.getText().isEmpty() || TxtCode.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor ingresa los Datos necesario.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (cbAuthor.getSelectedItem() == null || cbCategory.getSelectedItem() == null ) {
                JOptionPane.showMessageDialog(this, "Por favor selecciona La categoria y el Autor.", "Selección incompleta", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Book b = new Book();
            b.setTitle(TxtTitle.getText());
            b.setCode(TxtCode.getText());
            b.setAuthor((Author) cbAuthor.getSelectedItem());
            b.setCategory((Category) cbCategory.getSelectedItem());
          

            bookService.save(b);

            reload();

            JOptionPane.showMessageDialog(this, "Renta registrada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

       
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar el libro: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

